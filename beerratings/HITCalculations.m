#pragma mark - Slider 0.5 Steps
-(float)roundValue:(float)floatValue step:(double)step {
    return roundf(floatValue * 2.0) * step;
}

#pragma mark - farbe
-(UIColor*)getColorforEBC:(long)EBC {
    int SRM = EBC / 1.97 * 10;
    if (SRM < 0) {
        SRM = 0;
    }
    float Rot = srmtoRGB [SRM][0];
    float Gruen = srmtoRGB [SRM][1];
    float Blau = srmtoRGB [SRM][2];
    if (SRM > 300) {
        Rot = 0;
        Gruen = 0;
        Blau = 0;
    }
    if (Rot > 255 || Rot < 0) {
        Rot = 0;
    }
    if (Gruen > 255 || Gruen < 0) {
        Gruen = 0;
    }
    if (Blau > 255 || Blau < 0) {
        Blau = 0;
    }
    float nRot = Rot / 255;
    float nGruen = Gruen / 255;
    float nBlau = Blau / 255;
    
    UIColor *color = [[UIColor alloc]initWithRed:nRot green:nGruen blue:nBlau alpha:1];
    return color;
}
-(UIColor*) inverseColorWithRed:(CGFloat)red green:(CGFloat)green blue:(CGFloat)blue alpha:(CGFloat)alpha  {
    return [UIColor colorWithRed:1.-red green:1.-green blue:1.-blue alpha:alpha];
}
-(UIColor*) inverseColorWithUICollor:(UIColor*)color  {
    CGFloat red; CGFloat green; CGFloat blue; CGFloat alpha;
    [color getRed:&red green:&green blue:&blue alpha:&alpha];
    UIColor* retrn = [UIColor colorWithRed:1.-red green:1.-green blue:1.-blue alpha:alpha];
    return retrn;
}


